export function parseRedMasaiDemoMediaFlag(value: string | undefined) {
  return value === "true";
}

export type RedMasaiMediaType = "image" | "video";
export type RedMasaiMediaCategory =
  | "stay"
  | "experience"
  | "celebrate"
  | "create";
export type RedMasaiMediaApproval =
  | "private-demo"
  | "owner-approval-required";

export type RedMasaiMediaRecord = {
  key: string;
  src: string;
  type: RedMasaiMediaType;
  category: RedMasaiMediaCategory;
  alt: string;
  width: number;
  height: number;
  aspectRatio: `${number} / ${number}`;
  objectPosition: string;
  approval: RedMasaiMediaApproval;
  recognizablePeople: boolean;
  screenOrPosterWarning: boolean;
  commercialInclusionWarning: boolean;
  fallbackLabel: string;
  intendedPages: readonly string[];
  intendedSections: readonly string[];
};

const image = (
  record: Omit<RedMasaiMediaRecord, "type" | "aspectRatio">,
): RedMasaiMediaRecord => ({
  ...record,
  type: "image",
  aspectRatio: `${record.width} / ${record.height}`,
});

