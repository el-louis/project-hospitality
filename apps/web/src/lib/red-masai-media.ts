export type RedMasaiMediaKey = keyof typeof redMasaiMedia;

export const redMasaiMediaKeys = Object.keys(
  redMasaiMedia,
) as RedMasaiMediaKey[];

export function getRedMasaiMedia(key: RedMasaiMediaKey) {
  return redMasaiMedia[key];
}

export function hasUniqueRedMasaiMediaKeys() {
  return new Set(redMasaiMediaKeys).size === redMasaiMediaKeys.length;
}

